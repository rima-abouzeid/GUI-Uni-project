    private void getInfojButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getInfojButtonActionPerformed
        try {
            
            //connect
            connect();
            int id = Integer.parseInt(ownerComboBox.getSelectedItem().toString());

            //1. get owner by id
            String selectionQuery = "select * from owner where id=" + id;
            ResultSet rs = stmt.executeQuery(selectionQuery);

            
            //from class owner
            
            owner ow = null; // 3arrif object from class owner fadee

            //2. filling the object "ow" bl info ta3oul l owner yale na2ayto mnl database
            if (rs.next()) {
                ow = new owner();
                ow.setId(rs.getInt("id"));
                ow.setName(rs.getString("name"));
                ow.setPhoneNumber(rs.getInt("phoneNumber"));
                ow.setAddress(rs.getString("address"));      // yalle bee "" henne nafsel 3anewin yale ana 3amelton bl table
            }

            //ma7e yalle be alb l text fields lama a3mol delete
            if (ow != null) {
                idTextField.setText(ow.getId() + "");
                nameTextField.setText(ow.getName() + "");
                addressTextField.setText(ow.getAddress() + "");
                phoneNumberTextField.setText(ow.getPhoneNumber() + "");
            }

            close();
        } catch (SQLException ex) {
            Logger.getLogger(DeleteOwner.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_getInfojButtonActionPerformed

