    private void fillCombo() throws SQLException {
        

        ownerComboBox.removeAllItems();  //kermel lamma a3mol delete rou7 l id yalle 3meltello delete // awal shi bfadi kello berja3 b3abe l mawjoudin bl table
        connect();
        //get all ids
        String query = "Select * from owner";
        ResultSet result = stmt.executeQuery(query);
        
        //fill the ids in the comboBox
        while (result.next()) {
            ownerComboBox.addItem(result.getString("id"));            
        }
      close();
    }
